#include <bits/stdc++.h>

using namespace std;

int main(int argc, char* argv[])
{
    cout << fixed << setprecision(6);
    cout << atan(0.0) << endl;
    cout << atan(1E20) << endl;
    cout << atan(-1E20) << endl;
    cout << atan2(0, 1) << endl;
    cout << atan2(1, 0) << endl;
    cout << atan2(-1, 0) << endl;
    cout << atan2(0, -1) << endl;
    cout << atan2(-1E-10, -1) << endl;
    cout << atan2(0, 0) << endl;

    return 0;
}
